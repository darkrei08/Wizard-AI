#!/usr/bin/env bash
# setup.sh — Automated installation and setup script for the Wizard-AI environment.
# Supported Platform: Linux (x86_64 / aarch64) | macOS (x86_64 / arm64)
#
# After installation, the variable $WIZARD_AI_DIR is available in your shell
# and points to the directory where you cloned this repository.

set -euo pipefail

# ANSI color codes for premium terminal output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
CYAN='\033[0;36m'
BOLD='\033[1m'
NC='\033[0m' # No Color

echo -e "${CYAN}============================================================"
echo -e "         🧙‍♂️  Wizard-AI Environment Setup Wizard"
echo -e "============================================================${NC}"

# 0. Parse arguments
VERBOSE=0
YES_MODE=0
QUIET_OPT="--quiet"
for arg in "$@"; do
  case "$arg" in
    --verbose|-v) VERBOSE=1; QUIET_OPT="" ;;
    --yes|-y) YES_MODE=1 ;;
  esac
done

# 0. Enforce sudo and fix user environment
if [ "$EUID" -ne 0 ]; then
  echo -e "\n${RED}[ERROR] This script requires elevated privileges to install global dependencies (npm/cargo)."
  echo -e "Please run the installer with sudo:"
  echo -e "  sudo ./setup.sh${NC}\n"
  exit 1
fi

if [ -n "${SUDO_USER:-}" ]; then
  REAL_USER="$SUDO_USER"
  REAL_HOME=$(getent passwd "$REAL_USER" | cut -d: -f6)
  export HOME="$REAL_HOME"
  export USER="$REAL_USER"
  # Also override some env vars to prevent uv/cargo from defaulting to /root
  export CARGO_HOME="$REAL_HOME/.cargo"
  export UV_TOOL_DIR="$REAL_HOME/.local/share/uv/tools"
fi

# Resolve the absolute path of this script (= repo root, works even if symlinked)
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# 0. Save WIZARD_AI_DIR so skills and wrappers can reference the repo portably
echo -e "\n${BLUE}[0/10] Registering Wizard-AI installation path...${NC}"
mkdir -p "$HOME/.config/wizard-ai"
cat > "$HOME/.config/wizard-ai/env" <<EOF
# Wizard-AI environment — auto-generated by setup.sh on $(date '+%Y-%m-%d')
# Do not edit manually; re-run setup.sh to update.
export WIZARD_AI_DIR="$SCRIPT_DIR"
export PATH="\$HOME/.local/bin:\$PATH"
EOF

# Source the env file in the user's shell config if not already present
for RC in "$HOME/.bashrc" "$HOME/.zshrc" "$HOME/.profile"; do
  if [[ -f "$RC" ]] && ! grep -q "wizard-ai/env" "$RC"; then
    echo "" >> "$RC"
    echo "# Wizard-AI — load environment (added by setup.sh)" >> "$RC"
    echo 'source "$HOME/.config/wizard-ai/env"' >> "$RC"
    echo -e "  ${GREEN}✓ Added source line to $RC${NC}"
  fi
done
# Export for the current session
export WIZARD_AI_DIR="$SCRIPT_DIR"
export PATH="$HOME/.local/bin:$PATH"
echo -e "${GREEN}✓ WIZARD_AI_DIR=\"$WIZARD_AI_DIR\"${NC}"

# 1. Check/Install UV
echo -e "\n${BLUE}[1/10] Checking for modern Python & Package Manager (uv)...${NC}"
if ! command -v uv &>/dev/null; then
  echo -e "${YELLOW}uv not found. Installing uv via official script...${NC}"
  # SECURITY: Executing scripts directly from the internet via pipe is a security risk.
  # Ideally, verify the hash before execution in a production environment.
  curl -LsSf https://astral.sh/uv/install.sh | sh
  export PATH="$HOME/.local/bin:$PATH"
else
  echo -e "${GREEN}✓ uv is already installed: $(uv --version)${NC}"
fi

# Ensure local directories exist
mkdir -p "$HOME/.local/bin"
mkdir -p "$HOME/.ai-skills"

# 2. Recreate Python Virtual Environment for wrappers
echo -e "\n${BLUE}[2/10] Preparing Python Virtual Environment for LLMLingua & FlashRank...${NC}"
rm -rf "$HOME/.ai-skills/venv" 2>/dev/null || true
uv venv "$HOME/.ai-skills/venv" --seed $QUIET_OPT
VENV_PYTHON="$HOME/.ai-skills/venv/bin/python"

# Auto-install system dependencies for turbovec if possible
echo -e "${YELLOW}Checking system dependencies (Rust/Cargo & OpenBLAS) for turbovec...${NC}"
if command -v apt-get &>/dev/null; then
  if ! dpkg -s libopenblas-dev &>/dev/null || ! command -v cargo &>/dev/null; then
    echo -e "${YELLOW}Installing libopenblas-dev and cargo via apt-get...${NC}"
    apt-get update -yqq && apt-get install -yqq cargo libopenblas-dev
  fi
elif command -v pacman &>/dev/null; then
  if ! pacman -Qs openblas &>/dev/null || ! command -v cargo &>/dev/null; then
    echo -e "${YELLOW}Installing openblas and rust via pacman...${NC}"
    pacman -Sy --noconfirm rust openblas
  fi
elif command -v dnf &>/dev/null; then
  if ! rpm -q openblas-devel &>/dev/null || ! command -v cargo &>/dev/null; then
    echo -e "${YELLOW}Installing openblas-devel and cargo via dnf...${NC}"
    dnf install -y cargo openblas-devel
  fi
elif command -v brew &>/dev/null; then
  if ! sudo -u "$REAL_USER" brew list openblas &>/dev/null || ! command -v cargo &>/dev/null; then
    echo -e "${YELLOW}Installing openblas and rust via brew...${NC}"
    sudo -u "$REAL_USER" brew install rust openblas
  fi
fi

echo -e "${YELLOW}Installing llmlingua, flashrank, and turbovec inside the venv...${NC}"
uv pip install $QUIET_OPT --python "$VENV_PYTHON" \
  llmlingua flashrank transformers torch accelerate aisuite \
  "git+https://github.com/RyanCodrai/turbovec.git#subdirectory=turbovec-python" \
  || {
       echo -e "${RED}❌ Failed to install Python dependencies. Se turbovec fallisce, potresti non avere i compilatori C++/Rust o OpenBLAS installati.${NC}"
       echo -e "${RED}Per favore installa: rust (cargo) e openblas prima di eseguire lo script, es:${NC}"
       echo -e "${RED}Arch: sudo pacman -S rust openblas${NC}"
       echo -e "${RED}Ubuntu: sudo apt install cargo libopenblas-dev${NC}"
       exit 1
     }
echo -e "${GREEN}✓ Virtual environment ready at ~/.ai-skills/venv/${NC}"

